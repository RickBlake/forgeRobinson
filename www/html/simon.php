<p>
Né à Reading (Angleterre) en 1962, Simon fut présent dès l’âge de sept ans dans l’atelier de son
père. Il continua le talent multi-génerationnel en créant ses propres sculptures. Tandis que son
affinité avec le métal croissait, Simon poursuivit des études artistiques et s’intéressa vivement
dans la formation du métal, des bâtiments, et des manèges, dont beaucoup semblaient être des
sculptures monumentales.</p>

<p>
Simon étudie aux beaux-arts, puis s’associe avec son père, Antony, pendant cinq ans en tant
qu’apprenti. Cette période se conclut avec la commission du portail de Winchester: les tout
premiers portails de cette grandeur en inox au monde. Perpétuant la tradition européenne, Simon
commence son compagnonnage, començant en Tchécoslovaquie, où il travaille avec Alfred
Habermann, l’un des plus grands ferronniers artistiques dans le monde à l’époque. Il poursuit en
Allemagne, avec Hermann Gradinger, un autre grand maître, surtout dans le domaine de l’acier
profilé.</p>

<p>
En 1986, Simon devient le premier ferronnier de l’Ouest à présenter son oeuvre au Hefaiston
Symposium (Tchécoslovaquie). Il gagne le premier prix pour sa sculpture, Pax. Trois ans plus
tard, en 1989, au début de la Révolution de velours, il sera de nouveau présent à la conférence,
accompagné cette fois de son père.</p>

<p>
De retour en Angleterre, l’année suivante, Simon démarre sa propre entreprise. Au début, il crée
principalement des oeuvres ecclésiastiques pour l’église, par exemple, St. Mary-le-Bow dans le
quartier de Cheapside (Londres), entre autres.</p>

<p>
Il était devenu intéressé par l’acier inoxydable lors de son apprentissage; le matériau qui finira
par devenir sa spécialité. Il collabore souvent avec son père, avec un espace de travail partagé.
En 1994, père et fils ont une commission très complexe en inox d’un client européen. C’est un
tournant. Non seulement sur le point personnel pour Simon et Antony, mais aussi pour l’inox en
tant que matériau; les limites et les contraintes de l’inox sont élargis. Cette commission
comprend la création d’un grand nombre de pièces en inox: non seulement des poignées de porte
et des meubles du jardin, mais aussi des objets d’éclairage, des balustrades, plusieurs cheminées
et un portail. En 2003, père et fils gagnent la prestigieuse Tonypandy Cup pour cette suite de
travaux, aboutie après plus de six ans. Lors d’une cérémonie organisée par la Worshipful
Company of Blacksmiths (la Fraternité Vénérable des Forgerons), la médaille de bronze et le
titre de Maître Ferronnier sont décernés à Simon.</p>

<p>
Trois ans plus tard, Simon s’installe en Normandie; son entreprise est desormais baptisée Forge
Robinson. Il garde en vie la tradition familiale en créant des oeuvres uniques pour des clients
dans le monde entier, tout en répondant aux besoins des communautés locales et voisinantes. Il
continue de repousser et de redéfinir les frontières d’un art ancien, mais toujours vivant.</p>

<p>
* FWCB – Fellow Worshipful Company of Blacksmiths: Confrère de la Fraternité Vénérable des
Forgerons, membre de la Guilde</p>
